"""Definición del problema: calcular el area de un triangulo rectángulo.

Datos resultado: area de un triangulo rectángulo 

Datos de entrada: la base y la altura

Procedimiento:
area = (base*altura)/2"""

print("Programa que calcula el area de un triangulo rectangulo")

base = float(input("Ingrese la base: "))
altura = float(input("Ingrese la altura: "))

area = (base * altura)/2

print("El area del rectangulo es: ", area)

