numero1 = int(input("Digite el numero 1: "))
numero2 = int(input("Digite el numero 2: "))

r = numero1 + numero2
print("EL RESULTADO DE LA SUMA DE", numero1, "+", numero2, "=", r)

r = numero1 - numero2
print("EL RESULTADO DE LA resta DE", numero1, "-", numero2, "=", r)

r = numero1 * numero2
print("EL RESULTADO DE LA multiplicación DE", numero1, "*", numero2, "=", r)

r = numero1 / numero2
print("EL RESULTADO DE LA división  de: ", numero1, "/", numero2, "=", r)

r = numero1 // numero2
print("EL RESULTADO DE LA division entera de: ", numero1, "//", numero2, "=", r)

r = numero1 % numero2
print("EL RESULTADO DE residuo", numero1, "%", numero2, "=", r)

r = numero1 ** numero2 # Estoy elevando numero1 a la numero2
print("EL RESULTADO DE LA resta DE", numero1, "a la", numero2, "=", r)

